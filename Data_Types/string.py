strring = 'Hello World!'

print (strring)          # Prints complete string
print (strring[0])       # Prints first character of the string
print (strring[2:5])     # Prints characters starting from 3rd to 5th
print (strring[2:])      # Prints string starting from 3rd character
print (strring * 2)      # Prints string two times
print (strring + "TEST") # Prints concatenated string
